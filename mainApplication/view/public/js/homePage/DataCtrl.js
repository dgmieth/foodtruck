class DataCtrl {
    constructor() {
        this.months = ['January','February','March','April','May','June','July','August','September','October','November','December']
        this.events = [`2022-02-15`,`2022-02-18`,`2022-02-21`,`2022-02-24`,`2022-02-27`,`2022-03-02`,`2022-03-05`,`2022-03-08`,`2022-03-11`,`2022-03-14`,`2022-03-17`,`2022-03-20`,`2022-03-23`,`2022-03-26`,`2022-03-29`]
        this.today = '2022-02-12'
    }
    /*========================================================================================
    ========================================================================================
    ========================================================================================
                            CONFIGURE CALENDAR
    ========================================================================================
    ========================================================================================
    ========================================================================================*/
    fetchData(selectData){
        switch (selectData) {
            case 'events':
                return this.events
                break;
            case 'months':
                return this.months
                break;
            case 'today':
                return this.today
                break;
            default:
                break;
        }
    }
}